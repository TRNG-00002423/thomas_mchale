# Single responsibility princible: A class should have exactly one job 
# or responsibility within the software system

# Open/closed princible: open for extenstions, closed for modifications

# Liskov Substituation Principle: Objects of a superclass shall be replaced
#  with objects of its subclasses without breaking the application

# Interface Segregation Principle: No client should be forced to depend on 
# methods it does not use. It is better to have many small, specific interfaces,
# than one large general-purpose interface.
# in google class there are many different classes for each class you are a part of
# not one large google class

# Dependency Inversion Principle: High level modules should not depend on low
# level modules. Both should depend on abstraction(inerfaces).
# ex: the apple methodology vs windows, apple doesn't allow you to subsotute low
# level hardwear like ram or their cpu out, where windows you can use whatever
# components you want